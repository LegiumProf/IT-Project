const fallbackProjectSlug = "project-1";
window.projectCatalog = Object.entries(projectData).map(([slug, project]) => ({
  slug,
  ...project,
}));

function getProjectSlug() {
  const searchParams = new URLSearchParams(window.location.search);
  const slugFromQuery = searchParams.get("slug");

  if (slugFromQuery && projectData[slugFromQuery]) {
    return slugFromQuery;
  }

  const hashSlug = window.location.hash.replace("#", "");

  if (hashSlug && projectData[hashSlug]) {
    return hashSlug;
  }

  return fallbackProjectSlug;
}

function getProjectNumber(slug) {
  const match = slug.match(/project-(\d+)/);

  return match ? match[1] : "1";
}

function renderProjectHero(project) {
  const hero = document.querySelector("[data-project-hero]");
  const title = document.querySelector("[data-project-title]");
  const image = document.querySelector("[data-project-image]");
  const tags = document.querySelector("[data-project-tags]");

  if (!hero || !title || !image || !tags) return;

  document.title = `${project.title} | Vadim Semenov`;

  document.body.style.setProperty("--project-body-bg", project.pageBackgroundColor || "#0b0b0b");
  hero.style.setProperty("--project-page-bg", project.backgroundColor);
  hero.style.setProperty("--project-title-color", project.titleColor || "#ffffff");

  title.textContent = project.title;
  image.src = project.heroImage;
  image.alt = project.imageAlt || project.title;

  tags.innerHTML = "";

  project.tags.forEach(tagLabel => {
    const tag = document.createElement("span");

    tag.className = "project-hero__tag";
    tag.textContent = tagLabel;
    tags.append(tag);
  });
}

function renderProjectDescription(project) {
  const descriptionTitle = document.querySelector("[data-project-description-title]");
  const descriptionPrimary = document.querySelector("[data-project-description-text-primary]");
  const descriptionSecondary = document.querySelector("[data-project-description-text-secondary]");

  if (!descriptionTitle || !descriptionPrimary || !descriptionSecondary) return;

  descriptionTitle.textContent = project.descriptionTitle || project.title;
  descriptionPrimary.textContent = project.descriptionPrimary || "";
  descriptionSecondary.textContent = project.descriptionSecondary || "";
}

function renderProjectDecorative(projectNumber, project) {
  const decorativeImages = document.querySelectorAll("[data-project-decorative-image]");

  decorativeImages.forEach(image => {
    const imageIndex = image.dataset.projectDecorativeImage;

    image.src = `img/Project ${projectNumber}/Project ${projectNumber}_img${imageIndex}.png`;
    image.alt = `${project.title} decorative image ${imageIndex}`;
  });
}

function renderProjectTechnologies(project) {
  const technologiesRoot = document.querySelector("[data-project-technologies]");

  if (!technologiesRoot) return;

  technologiesRoot.innerHTML = "";

  (project.technologies || []).forEach((technology, index) => {
    const card = document.createElement("article");
    const number = document.createElement("p");
    const title = document.createElement("h3");
    const description = document.createElement("p");

    card.className = "project-technologies__card";
    number.className = "project-technologies__number";
    title.className = "project-technologies__title";
    description.className = "project-technologies__text";

    number.textContent = String(index + 1).padStart(2, "0");
    title.textContent = technology.title;
    description.textContent = technology.description;

    card.append(title, description, number);
    technologiesRoot.append(card);
  });
}

function renderProjectGallery(projectNumber, project) {
  const galleryImages = document.querySelectorAll("[data-project-gallery-image]");

  galleryImages.forEach(image => {
    const imageIndex = image.dataset.projectGalleryImage;

    image.src = `img/Project ${projectNumber}/Project ${projectNumber}_img${imageIndex}.png`;
    image.alt = `${project.title} gallery image ${imageIndex}`;
  });
}

const activeProjectSlug = getProjectSlug();
const activeProject = projectData[activeProjectSlug];
const activeProjectNumber = getProjectNumber(activeProjectSlug);

renderProjectHero(activeProject);
renderProjectDescription(activeProject);
renderProjectDecorative(activeProjectNumber, activeProject);
renderProjectTechnologies(activeProject);
renderProjectGallery(activeProjectNumber, activeProject);
